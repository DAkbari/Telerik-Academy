//Problem 6. Print Numbers
//Description => Write a program that prints the numbers 1, 101 and 1001, each on a separate line. 
//Input => There is no input for this task.
//Output => You program should print the numbers 1, 101 and 1001 on three separate lines.

using System;

class PrintNumbers
{
    static void Main()
    {
        Console.WriteLine("{0}\r\n{1}\r\n{2}", 1, 101, 1001);  // \r\n insert new line
    }
}