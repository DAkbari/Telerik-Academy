//Write a program to print the numbers 1, 101 and 1001, each at a separate line. Name the program correctly.

using System;

class PrintNumbers
{
    static void Main()
    {
        Console.WriteLine("{0}\r\n{1}\r\n{2}", 1, 101, 1001);       //  \r\n insert new line
    }
}